package com.Capgemini.OOPS.Encapsulation;

public class Student {

    // private data members
    private int id;
    private String name;

    // public setter methods
    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    // public getter methods
    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
